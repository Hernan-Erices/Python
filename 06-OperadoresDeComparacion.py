#operadores de compracion
# == es igual que
# != es distinto de
# < es menor que
# <= es menor o igual que
# > es mayor que
# >= es mayor o igual que
# devuelven True o False

es_igual_que = 5 == 6
print(es_igual_que)

es_distinto_de = 5 != 6
print(es_distinto_de)

es_menor_que = 7 < 4
print(es_menor_que)

es_menor_o_igual_que = 5 <= 5
print(es_menor_o_igual_que)

es_mayor_que = 7 > 5
print(es_mayor_que)

es_mayor_o_igual_que = 8 >= 9
print(es_mayor_o_igual_que)


#calculos combinados

a = 4
