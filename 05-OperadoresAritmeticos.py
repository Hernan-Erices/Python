#suma y resta (+ y -)

suma = 12 + 5
print(suma)

resta = 12 - 5
print(resta)

#multiplicacion y divisiones (* y /)

multiplicacion = 4 * 5
print(multiplicacion)

division = 34 / 2 #la division devuelve un dato float (flotante)
print(division)

#potenciacion (exponente) (**)
exponente = 14 ** 2
print(exponente)

#division baja (//)
division_baja = 34 // 4 #devuelve un entero redondeado hacia abajo ejemplo: si el resultado 2.4 devuelve 2.
print(division_baja)

#resto o modulo
modulo = 34 % 2
print (modulo)

tipo_de_dato = type(division) #type(dato) nos devuleve el tipo de dato
print (tipo_de_dato)
